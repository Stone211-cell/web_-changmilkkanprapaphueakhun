async function seed() {
  try {
    console.log("Starting seed...");
    
    // หาหรือสร้าง Category สำหรับบทความ
    let category = await prisma.category.findFirst({
      where: { name: "ความรู้ทั่วไปเกี่ยวกับประปา", type: "ARTICLE" }
    });
    
    if (!category) {
      category = await prisma.category.create({
        data: {
          name: "ความรู้ทั่วไปเกี่ยวกับประปา",
          type: "ARTICLE"
        }
      });
      console.log("Created category: ความรู้ทั่วไปเกี่ยวกับประปา");
    }

    let insertedCount = 0;
    
    for (const [index, article] of articlesData.entries()) {
      // สร้าง slug ที่ไม่ซ้ำกัน
      const slug = 'article-plumbing-tips-' + (index + 1) + '-' + Date.now();
      
      await prisma.article.create({
        data: {
          title: article.title,
          slug: slug,
          excerpt: article.excerpt,
          // เติมที่มาของภาพประกอบ
          content: article.content + '<p class="text-xs text-gray-400 mt-4"><em>(แหล่งที่มาภาพประกอบ: รูปภาพสุ่มจาก loremflickr สำหรับบทความความรู้)</em></p>',
          image: article.image,
          categoryId: category.id,
          author: "ช่างมิลการประปา",
          published: true
        }
      });
      insertedCount++;
    }
    
    console.log("Successfully seeded " + insertedCount + " articles!");
  } catch (error) {
    console.error("Error seeding data:", error);
  } finally {
    await prisma.$disconnect();
  }
}

seed();
